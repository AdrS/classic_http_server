void test_safe_path() {
	assert(safe_path(""));
	assert(safe_path("."));
	assert(safe_path(".dog"));
	assert(safe_path("./.dog"));
	assert(safe_path("./././././././././.dog"));
	assert(safe_path("hi/byte/.cars"));
	assert(safe_path("carrot.."));
	assert(safe_path("carrot../"));
	assert(safe_path("carrot..taco"));
	assert(safe_path("abc/.../cats"));

	assert(!safe_path("/"));
	assert(!safe_path(".."));
	assert(!safe_path("../cats"));
	assert(!safe_path("abc/../cats"));
	assert(!safe_path("abc/.."));
	assert(!safe_path("abc//"));
	assert(!safe_path("abc//def"));
}

